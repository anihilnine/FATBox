        public override bool SetObject(string propertyname, byte[] bytes)
        {
            textBox1.Text = BitConverter.ToString(bytes).Replace("-", ""); // hex
            textBox2.Text = System.Text.Encoding.ASCII.GetString(bytes).Replace("\0", "\r\n"); // string
            textBox3.Text = String.Join("\r\n", bytes.Select(x => x.ToString())); // dec
            return true;
        }
